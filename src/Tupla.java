public class Tupla {
    private String palavra;
    private int indice;
    public Tupla(String palavra) {
        this.palavra = palavra;
    }
    public String getPalavra() {
        return palavra;
    }

    public int getIndice(){
        return indice;
    }

    public void setIndice(int indice){
        this.indice = indice;
    }

}
